public class Operators {
    public static void main(String args[]){
        int A =10;
        int B =5;
        System.out.println("add="+(A+B));
        System.out.println("substract="+(A-B));
        
        int a=A++;
        int b=++A;
        System.out.println("postincrement="+ a);
        System.out.println("preincrement="+ b);


    }
}
